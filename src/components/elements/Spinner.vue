<template>
  <div :class="className" role="status">
    <span class="sr-only">{{ label }}</span>
  </div>
</template>
<script>
export default {
  props: {
    label: {
      type: String,
      default: "Loading..."
    },
    variant: {
      type: String,
      default: "border"
    },
    small: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    className() {
      let name = "spinner-" + this.variant;
      if (this.small) {
        name += " " + name + "-sm";
      }
      return name;
    }
  }
};
</script>
